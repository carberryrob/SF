function manualTrim(s) {
  return s.replace(/^\s+/, '').replace(/\s+$/, '');
}

var templatePD = job.getPrivateData("template_number");
templatePD = (templatePD + "").toUpperCase();
templatePD = manualTrim(templatePD);

var matchFound = false;

for (var i = 0; i < begins_template.length; i++) {
  var pattern = begins_template[i] + "";
  pattern = manualTrim(pattern).toUpperCase();

  if (templatePD.substring(0, pattern.length) === pattern) {
    matchFound = true;
    break;
  }
}

if (!matchFound) {
  for (var j = 0; j < template.length; j++) {
    var entry = template[j] + "";
    entry = manualTrim(entry).toUpperCase();

    if (templatePD === entry) {
      matchFound = true;
      break;
    }
  }
}

